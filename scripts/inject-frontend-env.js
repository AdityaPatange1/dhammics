#!/usr/bin/env node
/**
 * Reads DHAMMICS_API_BASE from the project root .env and writes
 * assets/js/env-config.js for the static frontend (no bundler).
 */
import { readFile, writeFile } from 'node:fs/promises';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const outFile = resolve(root, 'assets/js/env-config.js');

const defaultBase = 'http://127.0.0.1:8000/api/v1';
let url = process.env.DHAMMICS_API_BASE?.trim() || '';

try {
  const envPath = resolve(root, '.env');
  const raw = await readFile(envPath, 'utf8');
  for (const line of raw.split(/\r?\n/)) {
    const m = /^\s*DHAMMICS_API_BASE\s*=\s*(.*)$/.exec(line);
    if (m) {
      url = m[1].trim().replace(/^["']|["']$/g, '');
      break;
    }
  }
} catch {
  /* no .env */
}

if (!url) url = defaultBase;
url = url.replace(/\/$/, '');

const body = `/**
 * API base URL (includes /api/v1). Generated by scripts/inject-frontend-env.js
 * from root .env (DHAMMICS_API_BASE) or defaults for local development.
 */
export const DHAMMICS_API_BASE = ${JSON.stringify(url)};
`;

await writeFile(outFile, body, 'utf8');
console.log(`✓ ${outFile.replace(root + '/', '')} → DHAMMICS_API_BASE=${url}`);
