#!/usr/bin/env node
/**
 * Reads the project root `.env`, merges `DHAMMICS_*` from process.env, and writes
 * assets/js/env-config.js for the static frontend (no bundler).
 *
 * Only variables intended for the browser should use the DHAMMICS_ prefix.
 */
import { readFile, writeFile } from 'node:fs/promises';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const outFile = resolve(root, 'assets/js/env-config.js');

const DEFAULT_DHAMMICS_ENV = {
  DHAMMICS_API_BASE: 'http://127.0.0.1:8000/api/v1',
};

/**
 * Minimal dotenv parser (KEY=VALUE, # comments, optional quotes).
 * @param {string} content
 * @returns {Record<string, string>}
 */
function parseDotenv(content) {
  const out = {};
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue;
    let val = line.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

function stripTrailingSlash(url) {
  return String(url).replace(/\/$/, '');
}

const fileParsed = {};
try {
  const envPath = resolve(root, '.env');
  const raw = await readFile(envPath, 'utf8');
  const all = parseDotenv(raw);
  for (const [k, v] of Object.entries(all)) {
    if (k.startsWith('DHAMMICS_')) fileParsed[k] = v;
  }
} catch {
  /* no .env */
}

/** Defaults → .env (DHAMMICS_* only) → process.env DHAMMICS_* (CI/CD overlays file). */
const merged = { ...DEFAULT_DHAMMICS_ENV, ...fileParsed };
for (const [k, v] of Object.entries(process.env)) {
  if (v === undefined) continue;
  if (k.startsWith('DHAMMICS_')) merged[k] = v;
}

if (merged.DHAMMICS_API_BASE) {
  merged.DHAMMICS_API_BASE = stripTrailingSlash(merged.DHAMMICS_API_BASE);
}

const sortedKeys = Object.keys(merged).sort();
const sorted = {};
for (const k of sortedKeys) sorted[k] = merged[k];

const serialized = JSON.stringify(sorted, null, 2);
const body = `/**
 * Build-time public configuration. Generated by scripts/inject-frontend-env.js.
 * Source: root .env + process.env (keys starting with DHAMMICS_).
 * Do not put secrets here unless they are safe to expose in the browser bundle.
 */
export const DHAMMICS_ENV = Object.freeze(${serialized});
`;

await writeFile(outFile, body, 'utf8');

const keys = Object.keys(merged).sort();
console.log(`✓ ${outFile.replace(root + '/', '')} (${keys.length} keys: ${keys.join(', ')})`);
